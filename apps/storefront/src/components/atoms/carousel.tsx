type CarouselDotsProps = React.HTMLAttributes<HTMLDivElement> & {
  tone?: "light" | "dark";
};

export const CarouselDots = React.forwardRef<
  HTMLDivElement,
  CarouselDotsProps
>(({ className, tone = "light", ...props }, ref) => {
  const { scrollSnaps, selectedIndex, scrollTo } = useCarousel();
  const activeClass =
    tone === "dark" ? "bg-background" : "bg-foreground";
  const inactiveClass =
    tone === "dark"
      ? "bg-background/30 hover:bg-background/60"
      : "bg-foreground/20 hover:bg-foreground/40";
  return (
    <div
      ref={ref}
      className={cn("flex items-center justify-center gap-2", className)}
      {...props}
    >
      {scrollSnaps.map((_, index) => (
        <button
          key={index}
          type="button"
          aria-label={`Go to slide ${index + 1}`}
          onClick={() => scrollTo(index)}
          className={cn(
            "h-2 rounded-full transition-all duration-300",
            index === selectedIndex ? `w-8 ${activeClass}` : `w-2 ${inactiveClass}`
          )}
        />
      ))}
    </div>
  );
});
CarouselDots.displayName = "CarouselDots";

